package Clases.Usuario;

public class Domicilio {

    public String calle;
    public int altura;
    public int piso;
    public char departamento;

    public Domicilio(String calle, int altura, int piso, char departamento) {
        this.calle = calle;
        this.altura = altura;
        this.piso = piso;
        this.departamento = departamento;
    }
}
