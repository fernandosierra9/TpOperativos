package Clases;

public class Domicilio {

    private String calle;
    private int altura;
    private int piso;
    private char departamento;

    public Domicilio(String calle, int altura, int piso, char departamento) {
        this.calle = calle;
        this.altura = altura;
        this.piso = piso;
        this.departamento = departamento;
    }
}
